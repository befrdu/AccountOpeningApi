package citi.fintech.model;

public class Education {
	private String highestEducationLevel;
	private int yearsOfGraduation;
	private String university;
	public String getHighestEducationLevel() {
		return highestEducationLevel;
	}
	public void setHighestEducationLevel(String highestEducationLevel) {
		this.highestEducationLevel = highestEducationLevel;
	}
	public int getYearsOfGraduation() {
		return yearsOfGraduation;
	}
	public void setYearsOfGraduation(int yearsOfGraduation) {
		this.yearsOfGraduation = yearsOfGraduation;
	}
	public String getUniversity() {
		return university;
	}
	public void setUniversity(String university) {
		this.university = university;
	}
	
	
}
