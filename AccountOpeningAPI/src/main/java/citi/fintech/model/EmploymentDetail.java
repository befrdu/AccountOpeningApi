package citi.fintech.model;

public class EmploymentDetail {
	private String employerName;
	private String jobTitle;
	private String occupationCode;
	private String industryCode;
	private int employmentDurationInYears;
	private int employmentDurationInMonths;
	private String employmentStatus;
	private int yearsIntheIndustry;
	private int monthsInIndusty;
	public String getEmployerName() {
		return employerName;
	}
	public void setEmployerName(String employerName) {
		this.employerName = employerName;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getOccupationCode() {
		return occupationCode;
	}
	public void setOccupationCode(String occupationCode) {
		this.occupationCode = occupationCode;
	}
	public String getIndustryCode() {
		return industryCode;
	}
	public void setIndustryCode(String industryCode) {
		this.industryCode = industryCode;
	}
	public int getEmploymentDurationInYears() {
		return employmentDurationInYears;
	}
	public void setEmploymentDurationInYears(int employmentDurationInYears) {
		this.employmentDurationInYears = employmentDurationInYears;
	}
	public int getEmploymentDurationInMonths() {
		return employmentDurationInMonths;
	}
	public void setEmploymentDurationInMonths(int employmentDurationInMonths) {
		this.employmentDurationInMonths = employmentDurationInMonths;
	}
	public String getEmploymentStatus() {
		return employmentStatus;
	}
	public void setEmploymentStatus(String employmentStatus) {
		this.employmentStatus = employmentStatus;
	}
	public int getYearsIntheIndustry() {
		return yearsIntheIndustry;
	}
	public void setYearsIntheIndustry(int yearsIntheIndustry) {
		this.yearsIntheIndustry = yearsIntheIndustry;
	}
	public int getMonthsInIndusty() {
		return monthsInIndusty;
	}
	public void setMonthsInIndusty(int monthsInIndusty) {
		this.monthsInIndusty = monthsInIndusty;
	}
	
	
}
